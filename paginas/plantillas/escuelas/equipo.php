                        <h3>Beethoven<br>
                            <small>Profesor de Historia de la música</small>
                        </h3>
                        <p>Ludwig van Beethoven1 (Bonn, 16 de diciembre de 17702 -Viena, 26 de marzo de 1827) fue un compositor, director de orquesta y pianista alemán. Su legado musical abarca, cronológicamente, desde el período clásico hasta inicios del romanticismo musical. Fue uno de los compositores más preclaros e importantes de la historia de la música y su legado ha influido de forma decisiva en la evolución de la música.</p>
